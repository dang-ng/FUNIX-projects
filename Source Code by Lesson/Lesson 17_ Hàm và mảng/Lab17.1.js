function describeCountry(country, population, capticalCity) {
    return `${country} has ${population} million people and its capital city is ${capticalCity}`;
}

console.log(describeCountry('Finland', 6, 'Helsiki'));
console.log(describeCountry('Vietnam', 99, 'Hanoi'));
console.log(describeCountry('US', 300, 'WD'));